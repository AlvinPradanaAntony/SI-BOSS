package com.rafli.si_boss.model.update;

public class UserModel {
    private String nik_user, nama_user, tempat_lahir_user, tanggal_lahir_user, jenis_kelamin_user, alamat_user, no_hp_user, foto_user,
            email_user, password_user;

    public String getNik_user() {
        return nik_user;
    }

    public void setNik_user(String nik_user) {
        this.nik_user = nik_user;
    }

    public String getNama_user() {
        return nama_user;
    }

    public void setNama_user(String nama_user) {
        this.nama_user = nama_user;
    }

    public String getTempat_lahir_user() {
        return tempat_lahir_user;
    }

    public void setTempat_lahir_user(String tempat_lahir_user) {
        this.tempat_lahir_user = tempat_lahir_user;
    }

    public String getTanggal_lahir_user() {
        return tanggal_lahir_user;
    }

    public void setTanggal_lahir_user(String tanggal_lahir_user) {
        this.tanggal_lahir_user = tanggal_lahir_user;
    }

    public String getJenis_kelamin_user() {
        return jenis_kelamin_user;
    }

    public void setJenis_kelamin_user(String jenis_kelamin_user) {
        this.jenis_kelamin_user = jenis_kelamin_user;
    }

    public String getAlamat_user() {
        return alamat_user;
    }

    public void setAlamat_user(String alamat_user) {
        this.alamat_user = alamat_user;
    }

    public String getNo_hp_user() {
        return no_hp_user;
    }

    public void setNo_hp_user(String no_hp_user) {
        this.no_hp_user = no_hp_user;
    }

    public String getFoto_user() {
        return foto_user;
    }

    public void setFoto_user(String foto_user) {
        this.foto_user = foto_user;
    }

    public String getEmail_user() {
        return email_user;
    }

    public void setEmail_user(String email_user) {
        this.email_user = email_user;
    }

    public String getPassword_user() {
        return password_user;
    }

    public void setPassword_user(String password_user) {
        this.password_user = password_user;
    }
}
